function Find-AppIDs
{
	[CmdletBinding()]
	param ()
	
	try
	{
		# Get the registry paths for uninstall keys from both HKLM and HKU.
		# This includes 32-bit and 64-bit software on a 64-bit system.
		$uninstallRegistryPaths = (
			"HKLM:\Software\Microsoft\Windows\CurrentVersion\Uninstall",
			"HKLM:\SOFTWARE\Wow6432Node\Microsoft\Windows\CurrentVersion\Uninstall",
			(New-PSDrive -Name HKU -PSProvider Registry -Root Registry::HKEY_USERS -ErrorAction SilentlyContinue),
			(Get-ChildItem HKU: -ErrorAction SilentlyContinue | Where-Object { $_.Name -match 'S-\d-\d+-(\d+-){1,14}\d+$' })
		) | ForEach-Object { "HKU:\$($_.PSChildName)\Software\Microsoft\Windows\CurrentVersion\Uninstall" }
		
		# Process each uninstall key and collect the relevant information (GUID and DisplayName)
		# for each installed application that has a valid GUID.
		$uninstallRegistryPaths | ForEach-Object {
			Get-ChildItem -Path $_ -ErrorAction SilentlyContinue | Where-Object { $_.PSChildName -match '^{[A-Z0-9]{8}-([A-Z0-9]{4}-){3}[A-Z0-9]{12}}$' } | Select-Object @{ n = 'GUID'; e = { $_.PSChildName } }, @{ n = 'Name'; e = { $_.GetValue('DisplayName') } }
		}
	}
	catch [System.Exception] {
		# Handle exceptions and provide information about the error that occurred.
		Write-Error "An error occurred while running this script"
		Write-Error "Exception type: $($_.Exception.GetType().FullName)"
		Write-Error "Exception message: $($_.Exception.Message)"
	}
}

# Execute the Find-AppIDs function and export the results to a file named AppIDs.txt in the script's root directory.
Find-AppIDs | Out-File -FilePath $PSScriptRoot\AppIDs.txt -encoding ASCII

